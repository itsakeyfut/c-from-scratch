// 02_pointers.c — ポインタ基礎
// ビルド: clang -std=c11 -Wall -Wextra -o 02_pointers 02_pointers.c && ./02_pointers

#include <stdio.h>
#include <stddef.h>  // NULL

typedef struct {
    int x;
    int y;
} Point;

// ポインタ渡しで値を変更する例
void swap(int *a, int *b) {
    int tmp = *a;
    *a = *b;
    *b = tmp;
}

// 値渡し: コピーされるので呼び出し元は変わらない
void try_swap_by_value(int a, int b) {
    int tmp = a;
    a = b;
    b = tmp;
    printf("  関数内: a=%d, b=%d\n", a, b);
}

int main(void) {

    // =========================================================
    // & と * の基本
    // Rust の & に似ているが安全保証なし
    // =========================================================
    printf("=== & と * ===\n");
    int  x = 42;
    int *p = &x;          // &x: x のアドレスを取得
    printf("x のアドレス:   %p\n", (void *)&x);
    printf("p の値（アドレス）: %p\n", (void *)p);
    printf("*p（デリファレンス）: %d\n", *p);

    *p = 100;             // ポインタ経由で書き込み
    printf("*p = 100 後の x: %d\n", x);  // x も 100 になる

    // =========================================================
    // NULL ポインタ
    // Rust の None に相当する「何も指さない」状態
    // デリファレンスすると即クラッシュ（UB）
    // =========================================================
    printf("\n=== NULL ポインタ ===\n");
    int *null_p = NULL;
    printf("null_p == NULL: %d\n", null_p == NULL);
    // *null_p = 1;  // ← NG: セグフォルト

    // =========================================================
    // 構造体のポインタと -> 演算子
    // =========================================================
    printf("\n=== 構造体ポインタと -> ===\n");
    Point  pt  = {3, 4};
    Point *pp  = &pt;

    printf("ドット:    pt.x  = %d\n", pt.x);
    printf("アロー:    pp->x = %d\n", pp->x);      // (*pp).x と同じ
    printf("明示的:  (*pp).x = %d\n", (*pp).x);

    pp->x = 10;  // ポインタ経由で書き込み
    printf("pp->x = 10 後: pt.x = %d\n", pt.x);

    // =========================================================
    // 値渡し vs ポインタ渡し
    // =========================================================
    printf("\n=== 値渡し vs ポインタ渡し ===\n");
    int a = 1, b = 2;

    printf("値渡し前: a=%d, b=%d\n", a, b);
    try_swap_by_value(a, b);
    printf("値渡し後: a=%d, b=%d（変わらない）\n", a, b);

    printf("ポインタ渡し前: a=%d, b=%d\n", a, b);
    swap(&a, &b);
    printf("ポインタ渡し後: a=%d, b=%d（変わる）\n", a, b);

    // =========================================================
    // ポインタのポインタ（** ）
    // =========================================================
    printf("\n=== ポインタのポインタ ===\n");
    int  val  = 99;
    int *ptr  = &val;
    int **pptr = &ptr;  // ptr のアドレス

    printf("val   = %d\n", val);
    printf("*ptr  = %d\n", *ptr);
    printf("**pptr = %d\n", **pptr);

    **pptr = 77;  // 二段階でデリファレンスして書き込み
    printf("**pptr = 77 後: val = %d\n", val);

    // =========================================================
    // const とポインタ（4通りの組み合わせ）
    // =========================================================
    printf("\n=== const とポインタ ===\n");
    int n = 5;
    int m = 10;

    const int *cp  = &n;    // 指す先を変更不可、ポインタ自体は変更可
    int *const pc  = &n;    // ポインタ自体は変更不可、指す先は変更可
    // cp = &m;              // OK: ポインタを変え先を変える
    // *cp = 99;             // NG: 指す先は変更不可
    *pc = 99;               // OK: 指す先は変更可
    // pc = &m;              // NG: ポインタ自体は変更不可
    printf("n after *pc=99: %d\n", n);
    (void)cp; (void)m;

    // 関数の引数に const int * を使うと「読み取り専用で渡す」意味になる
    // → API 設計では読み取りのみの引数に const をつける習慣

    return 0;
}
